    public EditUser(int userId) throws SQLException {
        initComponents();
            
        userService= new UserService();
        user=userService.getUserById(userId);
       nameEdit.setText(user.getName());
      
        idEdit.setText(String.valueOf(user.getId()));
        
        phoneEdit.setText(user.getPhone());
        userNameEdit.setText(user.getUsername());
        passEdit.setText(user.getPass());
        aboutEdit.setText(user.getAbout());
        
        String favorite=user.getFavorite();
        if(favorite!=null){
           String[] fav= favorite.split(" ");
           for(String f: fav){
               if(f.equals("Music")){
                   musicEdit.setSelected(true);
               }
               if(f.equals("Game")){
                   gameEdit.setSelected(true);
               }
           }
        }
        String role=user.getRole();
        if(role!=null){
            if(role.equals("Admin"))
            roleAdminEdit.setSelected(true);
        }else {
            roleUserEdit.setSelected(true);
        }
        
    }

