    private void EditSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditSubmitActionPerformed
                // TODO add your handling code here:
        if(nameEdit.getText().equals("")){
    
             JOptionPane.showMessageDialog(this,"Enter your name!");

        }else if(userNameEdit.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Enter your User Name!");
        }
        else if(String.valueOf(passEdit.getPassword()).equals("")){
            JOptionPane.showMessageDialog(this,"Enter your PASSWORD!");       
        }else{
        user.setName(nameEdit.getText());
        user.setPhone(phoneEdit.getText());
        user.setUsername(userNameEdit.getText());
        user.setPass(String.valueOf(passEdit.getPassword()));
        user.setAbout(aboutEdit.getText());
        
        String favorite ="";
        if(gameEdit.isSelected()){
           favorite+=" Game "; 
        }
        if(musicEdit.isSelected()){
            favorite+=" Music ";
        }
        user.setFavorite(favorite);
        
        String role ="User";
        if(roleAdminEdit.isSelected()){
            role="Admin";
        }
        user.setRole(role);
        int userT = 0;
            try {
                userT = userService.updateUser(user);
            } catch (SQLException ex) {
                Logger.getLogger(EditUser.class.getName()).log(Level.SEVERE, null, ex);
            }
        
        if(userT == 1) { // Gọi hàm update bên service
            JOptionPane.showMessageDialog(this, "Sửa thông tin thành công", "Thông báo", JOptionPane.CLOSED_OPTION);
            try {
                new ListUser().setVisible(true);
            } catch (SQLException ex) {
                Logger.getLogger(EditUser.class.getName()).log(Level.SEVERE, null, ex);
            }
             this.dispose(); 
        } else {
            JOptionPane.showMessageDialog(this, "Sửa thông tin thất bại", "Thông báo", JOptionPane.CLOSED_OPTION);
        }

        
        }       
                
    }//GEN-LAST:event_EditSubmitActionPerformed

