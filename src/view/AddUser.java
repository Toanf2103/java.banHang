    private void submitAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitAddActionPerformed
        // TODO add your handling code here:

        if(nameAdd.getText().equals("")){
    
             JOptionPane.showMessageDialog(this,"Enter your name!");

        }else if(userNameAdd.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Enter your User Name!");
        }
        else if(String.valueOf(passAdd.getPassword()).equals("")){
            JOptionPane.showMessageDialog(this,"Enter your PASSWORD!");       
        }
        else{
            user.setName(nameAdd.getText());
            user.setPhone(phoneAdd.getText());
            user.setUsername(userNameAdd.getText());
            user.setPass(String.valueOf(passAdd.getPassword()));
            user.setAbout(aboutAdd.getText());

            String favorite ="";
            if(gameAdd.isSelected()){
               favorite+="Game"; 
            }
            if(musicAdd.isSelected()){
                favorite+=" Music";
            }
            user.setFavorite(favorite);

            String role ="User";
            if(roleAdminAdd.isSelected()){
                role="Admin";
            }
            user.setRole(role);
            userService.addUser(user);
            new ListUser().setVisible(true);
            this.dispose();
        }
        
    }//GEN-LAST:event_submitAddActionPerformed

